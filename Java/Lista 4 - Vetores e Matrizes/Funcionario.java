public class Funcionario {
    
    int matricula;
    String nome;
    int departamento;
    float salario;
    String funcao;

    public Funcionario(int matricula, String nome, int departamento, float salario, String funcao) {
        this.matricula = matricula;
        this.nome = nome;
        this.departamento = departamento;
        this.salario = salario;
        this.funcao = funcao;
    }
    
    
    
}
